// colors.h
// 颜色主题定义文件：糖果色 & 深色色卡（用于文字、边框、按钮背景等）

#ifndef COLORS_H
#define COLORS_H

// ---------------------- 糖果色色卡（Candy Colors） ----------------------
#define CANDY_PINK     0xFC18  // RGB: 255, 102, 204
#define CANDY_BLUE     0xAEDC  // RGB: 170, 221, 255
#define CANDY_PURPLE   0xC93C  // RGB: 204, 153, 255
#define CANDY_GREEN    0x8FE7  // RGB: 153, 255, 204
#define CANDY_YELLOW   0xFFF6  // RGB: 255, 255, 102
#define CANDY_ORANGE   0xFDA0  // RGB: 255, 170, 102

// ---------------------- 深色色卡（Deep Colors） ----------------------
#define DEEP_PURPLE    0x780F  // RGB: 85, 0, 153
#define DEEP_BLUE      0x001F  // RGB: 0, 0, 255
#define DEEP_GREEN     0x03E0  // RGB: 0, 255, 0
#define DEEP_RED       0xF800  // RGB: 255, 0, 0
#define DEEP_BROWN     0x6208  // RGB: 100, 40, 40
#define DEEP_GRAY      0x7BEF  // RGB: 123, 123, 123

// ---------------------- 中性色 ----------------------
#define COLOR_WHITE    0xFFFF
#define COLOR_BLACK    0x0000
#define COLOR_LIGHTGRAY 0xC618
#define COLOR_GRAY     0x8410

#endif
